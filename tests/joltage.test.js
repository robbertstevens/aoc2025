import {test, expect} from 'vitest';
import {joltage} from "../src/day-3";

test.each([
    ["987654321111111", 98n],
    ["811111111111119", 89n],
    ["234234234234278", 78n],
    ["818181911112111", 92n],
])('%s', (input, expected) => {
    const result = joltage(input, [0, 0])

    expect(result).toBe(expected)
})

